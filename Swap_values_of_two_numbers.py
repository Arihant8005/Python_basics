Num1 = int(input("Enter NUM 1 : "))
Num2 = int(input("Enter NUM 2 : "))

print("Before swapping : ")
print("Num1 = " , Num1)
print("Num2 = " , Num2)

temp = Num1
Num1 = Num2
Num2 = temp

print("After swaping : ")
print("Num 1 = ", Num1)
print("Num 2 = ", Num2)

